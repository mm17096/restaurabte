<h1>COMO REGISTRAR ME</h1>

<h4>
    <div>
        Para poder registrarse de clik en el boton "Registrarse", el cual lo llevara a una vista donde
        debera llenar un formulario con sus datos personales ahi indicados, y asi poder crear un usuario
        personalizado el cual le servira para poder hacer uso de nuestros productos y servicios.
        <br />
        (La foto de perfil es opcional)
        <br /><br />
        Otras Indicaciones:
        <br />
        La contraseña debe tener como minimo 6 caracteres y como maximo 10
        <br />
        Entre los datos que debe llenar se encuentran algunos especificos que deben ser necesariamente correctos como:
        <br /><br />
        ·El número de DUI del cliente debe ser correcto ya que sera necesario para que el cliente pueda recuperar la cuenta de usuario que
        ha creado, sea el caso que haya olvidado la contraseña; pero el cliente esta en total libertad de no colocar un numero de DUI correcto,
        pero si una combinacion de digitos que pueda recordar ya que la combinacion servira para recuperar su cuenta ya sea el caso.
        <br /><br />
        ·El Telefono de cliente, ya quesera un medio de comunicación entre el cliente y repartidor del producto que el cliente
        halla solicitado, si llegace ha haver necesidad deconfirmar la ubicacion de entrega o si el repartidor requiere una mejor ubicación.
        <br /><br />
        ·El correo electronico del cliente debe ser uno que este en uso y preferiblemente que sea por la persona, ya que despues de crear la cuenta de
        usuario habra la necesidad de confirmar la cuenta mediante un enlace que se le enviara al correo electronico especificado, ademas tambien
        servira para la recuperacion de la misma cuenta si fuera el caso.
        <br /><br />
        ·Las coordenadas de la ubicacion del usuario son necesarias para poder ubicar al mismo y poder hacer llegarle su pedido hasta esa ubicacion;
        recomendamos como empresa que haga uso de la opcion "Obtener Ubicacion" donde podra colocar una ubicacion por defecto donde considere que puede
        recibir los productos, tambien en cualquier momento una ves creado y confirmado su usuario podra usarlo y poder modificar sus datos incluida la
        ubicacion por cualquier incombeniente que desee recibir los poductos en otro lado
</h4>
