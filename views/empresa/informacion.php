<h1>COMO REALIZAR UN PEDIDO</h1>
<h4>
    Para poder realizar un pedido nada mas es necesario "agregar los productos al carrito" en donde la cantidad minima de compra es de $15.0
    por razones de costos de envio las cuales son unicamente validas en San Vicente centro y sus alrededores.
    <br /><br />
    Las formas de pago son por medio de la plataforma de pagos PayPal o puede canselar al recibir el pedido.
